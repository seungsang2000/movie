package egovframework.kss.main.vo;

public class DivisionVO {
	private int id;
	private String name;

	/**
	 * @id@ getter
	 * @return	id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @id@ setter
	 * @param	id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @name@ getter
	 * @return	name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @name@ setter
	 * @param	name
	 */
	public void setName(String name) {
		this.name = name;
	}

	public DivisionVO() {

	}

}
